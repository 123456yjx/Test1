/*******************************************************************************
* 函数名 : Modbus_SendData
* 描述   : 接收串口收到的数据函数
* 输入   : Package_type传递包类型，Data_Msg传递数据区
* 输出   : void
* 返回   : 
* 注意   : 
*******************************************************************************/
void Parameter_SendData(uint8_t Package_type ,uint16_t Data_Msg)
{
  unsigned char Send_Data[7];/*数据的缓存器*/
	unsigned char buf[2];//数据区的缓存区
	int i;

	if(Send_Statflag==1)
	{
		  Send_Statflag=0;
			Send_Data[0]=0xAA;//帧头数据
			Send_Data[1]=Package_type;
			Send_Data[2]=Data_Msg>>8;
			Send_Data[3]=Data_Msg&0xff;
			for(i=0;i<2;i++)/*获取数据区的数据,得到校验位*/
			{
				  buf[i]=Send_Data[i+2];
			}
			Send_Data[4]=CRC16(buf,2)>>8;//取CRC的高八位
			Send_Data[5]=CRC16(buf,2)&0xFF;//取CRC的低八位
			Send_Data[6]=0x55;
			Usart_SendArray(USART2,Send_Data,7);//串口1发送数据
	}  
}



