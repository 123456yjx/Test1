uint16_t CRC16(const uint8_t *u8PtrMsg, uint16_t u16DataLen) {
	uint8_t l_u8CRCHi = 0xFF ;
	uint8_t l_u8CRCLo = 0xFF ;
	uint8_t l_u8Index ;
	while (u16DataLen--) {
		l_u8Index = l_u8CRCHi ^ *u8PtrMsg++ ;
		l_u8CRCHi = l_u8CRCLo ^ g_u8CRCHiTb[l_u8Index] ;
		l_u8CRCLo = g_u8CRCLoTb[l_u8Index] ;
	}
	return (l_u8CRCHi << 8 | l_u8CRCLo) ;
}
 
